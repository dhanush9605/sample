/**
 * Utility functions for mathematical operations.
 * Each function is designated to a specific developer for implementation.
 */

// Developer 1
export function add(a, b) {
  return a + b;
}

// Developer 2
export function subtract(a, b) {
  return a - b;
}

// Developer 3
export function multiply(a, b) {
  return a * b;
}

// Developer 4
export function divide(a, b) {
  if (b === 0) {
    return 'Error';
  }
  return a / b;
}
